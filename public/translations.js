function mergeLanguageExtras(extrasByLanguage) {
    Object.entries(extrasByLanguage).forEach(([lang, extras]) => {
        window.LANGUAGES[lang] = { ...(window.LANGUAGES[lang] || {}), ...extras };
    });
}

window.UNIVERSAL_UI_EXTRAS = UNIVERSAL_UI_EXTRAS;
