mergeLanguageExtras(UNIVERSAL_UI_EXTRAS);

/**
 * 🌐 Global Translation Engine
 * Scans DOM for data-t and data-i18n attributes and applies translations.
 */
function applyLanguage() {
    try {
        const lang = localStorage.getItem('axp_lang') || 'en';
        const dict = (window.LANGUAGES && window.LANGUAGES[lang]) || {};
        const fallbackDict = (window.LANGUAGES && window.LANGUAGES.en) || {};
        
        // 1. Scan for data-t (legacy / specific translations)
        document.querySelectorAll('[data-t]').forEach(el => {
            const key = el.getAttribute('data-t');
            const value = dict[key] || fallbackDict[key];
            if (value) {
                el.textContent = value;
            }
        });

        // 2. Scan for data-i18n (new standard)
        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            const value = dict[key] || fallbackDict[key];
            if (value) {
                el.textContent = value;
            }
        });

        // 3. Update document lang attribute
        document.documentElement.lang = lang;
        
        console.log(`--- [I18N] APPLIED: ${lang.toUpperCase()} ---`);
    } catch (e) {
        console.error('I18N_ERROR:', e);
    }
}

// Auto-run if in a browser environment
if (typeof document !== 'undefined') {
    if (document.readyState === 'loading') {
        document.addEventListener('DOMContentLoaded', applyLanguage);
    } else {
        applyLanguage();
    }
}
